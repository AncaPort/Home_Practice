package home_practice;

public class Java18 {

    public static void main(String[] args) {

        int s1 = 125;
        int s2 = 24;

        int sum = s1 + s2;
        System.out.println(+s1 + "+" + s2 + "=" + sum);

        int sub = s1 - s2;
        System.out.println(s1 + "-" + s2 + "=" + sub);

        int multi = s1 * s2;
        System.out.println(s1 + "*" + s2 + "=" + multi);

        int divide = s1 / s2;
        System.out.println(s1 + "/" + s2 + "=" + divide);

        int mod = s1 % s2;
        System.out.println(s1 + " mod " + s2 + "=" + mod);


    }
}
